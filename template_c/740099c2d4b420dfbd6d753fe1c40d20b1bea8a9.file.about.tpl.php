(function() {
	var devlist = $('#J_dev-list'),
		childs = devlist.children(),
		length = childs.length,
		index = 0,
		tops = [],
		time = 3000,
		interval = '',
		interHanlder = new Function();
	$.each(childs, function (idx, child) {
		tops[idx] = $(child).height();
	});

	(interHanlder = function () {
		interval = setInterval(function () {
			devlist.animate({ scrollTop: tops[index] + 50 }, time - 1500, function () {
				childs = devlist.children();
				childs.first().clone().appendTo(devlist);
				childs.first().remove();
				devlist.scrollTop(0);
				index++;
				if (index == length) {
					index = 0;
				}
			});
		}, time);
	}) ();

	devlist.mouseover(function (evt) {
		interval ? clearInterval(interval) : '';
	}).mouseout(function (evt) {
		interHanlder();
	});
})();
